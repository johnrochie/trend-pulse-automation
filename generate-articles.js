const axios = require('axios');
const database = require('./database');
require('dotenv').config();

class ArticleGenerator {
  constructor() {
    this.apiKey = process.env.DEEPSEEK_API_KEY;
    this.baseUrl = process.env.DEEPSEEK_BASE_URL || 'https://api.deepseek.com';
    this.maxArticles = parseInt(process.env.MAX_ARTICLES_PER_CYCLE) || 5;
  }

  async generateArticleContent(article) {
    try {
      console.log(`🤖 Generating AI content for: ${article.title.substring(0, 60)}...`);
      
      const prompt = this.createPrompt(article);
      
      const response = await axios.post(
        `${this.baseUrl}/chat/completions`,
        {
          model: 'deepseek-chat',
          messages: [
            {
              role: 'system',
              content: 'You are a professional news writer and analyst for Trend Pulse, an AI-powered news site. Write engaging, informative news articles with analysis and context.'
            },
            {
              role: 'user',
              content: prompt
            }
          ],
          max_tokens: 1500,
          temperature: 0.7
        },
        {
          headers: {
            'Authorization': `Bearer ${this.apiKey}`,
            'Content-Type': 'application/json'
          }
        }
      );

      const aiContent = response.data.choices[0]?.message?.content;
      
      if (!aiContent) {
        throw new Error('No content generated by AI');
      }

      return this.formatArticle(article, aiContent);
    } catch (error) {
      console.error(`❌ Error generating content for article ${article.id}:`, error.message);
      return null;
    }
  }

  createPrompt(article) {
    return `Write a comprehensive news article based on this breaking news:

ORIGINAL HEADLINE: ${article.title}
SOURCE: ${article.source_name}
CATEGORY: ${article.category}
PUBLISHED: ${new Date(article.published_at).toLocaleDateString()}
DESCRIPTION: ${article.description}

Please write an 800-1200 word article that includes:

1. HEADLINE: A compelling, SEO-friendly headline (different from original)
2. INTRODUCTION: Brief overview and why this matters
3. KEY FACTS: What happened, who's involved, important details
4. ANALYSIS: Context, implications, expert perspectives
5. WHAT'S NEXT: Predictions, upcoming developments, future impact
6. RELATED TRENDS: How this connects to other trends in ${article.category}
7. CONCLUSION: Summary and key takeaways

Write in a professional, engaging news style. Include subheadings, bullet points where appropriate, and make it easy to read.

Also suggest 3-5 relevant tags for this article.

IMPORTANT: Do not include any markdown formatting, just plain text with clear section breaks.`;
  }

  formatArticle(article, aiContent) {
    // Extract tags if mentioned in the content
    const tagMatch = aiContent.match(/TAGS?:?\s*([^\n]+)/i);
    const tags = tagMatch ? tagMatch[1].split(',').map(tag => tag.trim()).filter(tag => tag) : [];
    
    // Ensure we have some default tags
    if (tags.length === 0) {
      tags.push(article.category, 'news', 'analysis');
    }

    // Create the full article content
    const fullContent = `
${aiContent}

---
*Article generated by AI based on reporting from ${article.source_name}. Original story: ${article.url}*
*Published on Trend Pulse - AI-Powered Real-Time News & Trends*
    `.trim();

    return {
      content: fullContent,
      tags: tags.slice(0, 5).join(', '),
      wordCount: fullContent.split(/\s+/).length,
      generatedAt: new Date().toISOString()
    };
  }

  async generateBatch() {
    try {
      console.log('🚀 Starting AI article generation...');
      
      // Get articles that need AI content
      const articles = await database.getArticlesToGenerate(this.maxArticles);
      
      if (articles.length === 0) {
        console.log('✅ No articles need AI generation. All caught up!');
        return [];
      }

      console.log(`📝 Found ${articles.length} articles to generate`);
      
      const generatedArticles = [];
      
      for (const article of articles) {
        console.log(`\n--- Processing Article ${article.id} ---`);
        console.log(`📰 ${article.title}`);
        
        const aiResult = await this.generateArticleContent(article);
        
        if (aiResult) {
          // Update database with AI content
          await database.updateArticleWithAIContent(article.id, aiResult.content);
          
          generatedArticles.push({
            id: article.id,
            title: article.title,
            wordCount: aiResult.wordCount,
            tags: aiResult.tags
          });
          
          console.log(`✅ Generated ${aiResult.wordCount} words`);
          console.log(`🏷️  Tags: ${aiResult.tags}`);
          
          // Small delay to avoid rate limiting
          await new Promise(resolve => setTimeout(resolve, 2000));
        } else {
          console.log(`⚠️  Skipping article ${article.id} due to generation error`);
        }
      }
      
      return generatedArticles;
    } catch (error) {
      console.error('❌ Generation batch failed:', error);
      throw error;
    }
  }

  async run() {
    try {
      console.log('='.repeat(60));
      console.log('🤖 TREND PULSE - AI ARTICLE GENERATOR');
      console.log('='.repeat(60));
      
      const generated = await this.generateBatch();
      
      // Get updated stats
      const stats = await database.getStats();
      
      console.log('\n📈 GENERATION CYCLE COMPLETE');
      console.log('='.repeat(60));
      console.log(`✅ Articles generated: ${generated.length}`);
      console.log(`📊 Database stats:`);
      console.log(`   Total articles: ${stats.total_articles}`);
      console.log(`   Fetched: ${stats.fetched}`);
      console.log(`   Generated: ${stats.generated}`);
      console.log(`   Published: ${stats.published}`);
      console.log('='.repeat(60));
      
      if (generated.length > 0) {
        console.log('\n🎯 Generated articles:');
        generated.forEach(article => {
          console.log(`   • ${article.title.substring(0, 60)}... (${article.wordCount} words)`);
        });
      }
      
      return generated;
    } catch (error) {
      console.error('💥 Generation cycle failed:', error);
      throw error;
    }
  }
}

// Run if called directly
if (require.main === module) {
  const generator = new ArticleGenerator();
  generator.run().then(() => {
    console.log('🎯 Generation complete. Next: Publish articles to site.');
    process.exit(0);
  }).catch(error => {
    console.error('💥 Fatal error:', error);
    process.exit(1);
  });
}

module.exports = ArticleGenerator;